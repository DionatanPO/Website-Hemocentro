		<h4>Condições básicas para doar sangue.</h4>
		<ul class="i">
		<li >Sentir-se bem, com saúde.</li>
		<li>Ter entre 16 e 69 anos de idade e peso acima de 50kg.</li>
		<li>Apresentar documento com foto, emitido por órgão
		oficial e válido em todo o território nacional.</li>
		</ul>

		<h4>Recomendações para o dia da doação.</h4>
		<ul class="i">
		<li>Nunca ir doar sangue em jejum.</li>
		<li>Fazer um repouso mínimo de 6 horas na noite anterior à doação.</li>
		<li>Não ingerir bebidas alcoólicas nas 12 horas anteriores à doação.</li>
		<li>Evitar fumar por pelo menos duas horas antes da doação.</li>
		<li>Evitar ingerir alimentos gordurosos.</li>
		</ul>

		<h4>Quem não pode doar?</h4>
		<ul class="i">
		<li>Quem teve diagnóstico de hepatite após os 11 anos de idade. </li>
		<li>Mulheres grávidas ou que estejam amamentando. </li>
		<li>Pessoas que estão expostas a doenças transmissíveis pelo
		sangue, como aids, hepatite, sífilis e doença de Chagas.</li>
		</ul>

